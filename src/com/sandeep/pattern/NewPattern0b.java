package com.sandeep.pattern;

public class NewPattern0b {
        public static void main(String[] args) {
            for(int i =5 ; i>= 1; i--){
                for (int j = i; j<= 5 ; j++){
                    System.out.print("4");
                }
                System.out.println();
            }

            for(int i =1 ; i<= 6; i++){
                for (int j = i; j<= 6 ; j++){
                    System.out.print("4");
                }
                System.out.println();
            }

        }
    }

