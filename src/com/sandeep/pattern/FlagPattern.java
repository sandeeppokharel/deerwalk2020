package com.sandeep.pattern;

public class FlagPattern {
    public static void main(String[] args) {
            for(int i =5 ; i>= 1; i--){
                for (int j = i; j<= 5 ; j++){
                    System.out.print("4");
                }
                System.out.println();
            }
        for(int i =5 ; i>= 1; i--){
            for (int j = i; j<= 5 ; j++){
                System.out.print("4");
            }
            System.out.println();
        }

        }
}